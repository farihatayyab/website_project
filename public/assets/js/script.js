document.addEventListener('DOMContentLoaded', function() {
    const trigger = document.getElementById('collapsible-trigger-1740039533515-2');
    const content = document.getElementById('collapsible-content-1740039533515-2');
    const icon = document.getElementById('icon-toggle-2');

    trigger.addEventListener('click', function() {
        const isClosed = trigger.classList.contains('is-closed');
        if (isClosed) {
            // Open the collapsible
            trigger.classList.remove('is-closed');
            trigger.setAttribute('aria-expanded', 'true');
            content.style.height = content.scrollHeight + 'px';
            icon.setAttribute('xlink:href', '#edit-minus');
        } else {
            // Close the collapsible
            trigger.classList.add('is-closed');
            trigger.setAttribute('aria-expanded', 'false');
            content.style.height = '0px';
            icon.setAttribute('xlink:href', '#edit-plus');
        }
    });
});

